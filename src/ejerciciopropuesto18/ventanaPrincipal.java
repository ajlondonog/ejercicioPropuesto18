    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int nhtm;
        double vht;
        int retencion;
        double salarioBruto;
        double salarioNeto;
        
        nhtm = Integer.parseInt(txtNHTM.getText());
        vht = Double.parseDouble(txtVHT.getText());
        retencion = Integer.parseInt(txtRetencion.getText());
        
        Nomina n = new Nomina(nhtm, vht, retencion);
        
        salarioBruto = n.calcularSalarioBruto();
        salarioNeto = n.calcularSalarioNeto();
        
        txtNHTM.setText(String.valueOf(nhtm));
        txtVHT.setText(String.valueOf(vht));
        txtRetencion.setText(String.valueOf(retencion));
        txtSalarioBruto.setText(String.valueOf(salarioBruto));
        txtSalarioNeto.setText(String.valueOf(salarioNeto));
    }//GEN-LAST:event_btnCalcularActionPerformed

